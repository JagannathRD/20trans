package com.xworkz.typecast;

public class CharToInt {
	public static void main(String[] args) {
		System.out.println("MMS");
		char c = 'A';
		int i = c;
		System.out.println("Integer for char " + c + " = " + i);

		int j =70;
		char d = (char) j;
		System.out.println("Char for int " + j + " = " + d);

		System.out.println("MME");
	}
}
